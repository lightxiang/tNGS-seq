// 重新计算占比
function count_rate(items, type) {
  let reads = 0
  var item
  for (item of items) {
      reads += parseInt(item["uRPTM"])
  }
  let new_items = []
  for (item of items) {
      item['reads_percent'] = (item['uRPTM'] / reads * 100).toFixed(2) + '%'
      if (item['class'] === type ) {
          new_items.push(item)
      }
  }
  new_items.sort((a, b) => (parseInt(b['uRPTM']) - parseInt(a['uRPTM'])))
  return new_items
}

