function get_cols(item, type) {
  let coL_names = ["Type", "C_name_g", "C_name_s", "L_name", "uRPTM", "Intro"]
  let new_item = {}
  for (let key in item) {
      if (coL_names.includes(key)) {
          new_item[key] = item[key]
      }
  }
  new_item['class'] = type
  return new_item
}

